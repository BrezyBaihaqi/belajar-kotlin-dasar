/** Initializer Block
 * initializer block adalah blok kode yg akan di eksekusi ketika Constructor dipanggil
 * kita bisa memasukkan kode program di dalam initializer block
 *
 * Sebelumnya, kita hanya membuat parameter Constructor lalu mengassign ke dalam Properties
 * lalu bagaimana jika kita ingin menuliskan kode complex di dalam Constructor
 * defaultnya tidak bisa menambahkan kode di dalam body Class
 * karena di dalam body Class kita hanya bisa mendefinisikan Properties dan Functionyya
 *
 * tapi jika kita mau menambahkan  kode di dalam Constructor, kita menggunakan initializer block
 * gimana caranya?
 *
 * kita gunakan kata kunci init { kode } di dalam block Class,
 * saat di eksekusi, kode program di dalam init { } yg akan di eksekusi
 *
 * -> cek Class Car
 */